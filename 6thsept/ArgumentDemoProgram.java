class ArgumentDemoProgram  //Declaring the ArgumentDemoProgram class 
{ // ArgumentDemoProgram class start with this curly brace
	
	public static void main(String[]args)  //Declaring the main Method 
	
	{ // Execution start of the main method 
	
	
	
		//printing the string Hello & then Take a input from the user
		
		System.out.println("Hello "+ args[0]); // args [0]= sweety (This input taking from the user) & printing it
		
		System.out.println("How are you ?  The answer is  --------------" + args[1] + args[2] + args[3]); //take the input from the user & printing it both
		
		
	} // end of the main Method
	
} // End of the ArgumentDemoProgram class 